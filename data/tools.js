export const getToolById = (id) => tools.find((t) => t.id === id);

export const searchTools = (query) => {
  if (!query || query.trim() === "") return tools;
  const q = query.toLowerCase();
  return tools.filter(
    (t) =>
      t.name.toLowerCase().includes(q) ||
      t.tags.some((tag) => tag.includes(q)) ||
      t.description.toLowerCase().includes(q) ||
      (t.category && t.category.toLowerCase().includes(q))
  );
};

export const getRelatedTools = (toolId, max = 3) => {
  const tool = getToolById(toolId);
  if (!tool) return tools.slice(0, max);
  return tools
    .filter(
      (t) =>
        t.id !== toolId &&
        (t.category === tool.category ||
          t.tags.some((tag) => tool.tags.includes(tag)))
    )
    .slice(0, max);
};

export const featuredTools = tools.filter((t) => t.featured);

export const categories = ["All", ...new Set(tools.map(t => t.category))];


